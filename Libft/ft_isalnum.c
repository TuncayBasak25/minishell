#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (8);
	else if (c >= '0' && c <= '9')
		return (8);
	else if (c == -2147483648)
		ft_isalnum(c);
	return (0);
}
