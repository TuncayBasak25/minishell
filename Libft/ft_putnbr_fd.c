#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > -2147483648 && n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n != -2147483648 && n < 10)
	{
		ft_putchar_fd(n + '0', fd);
	}
	else if (n != -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		write(fd, "-2147483648", 11);
}
